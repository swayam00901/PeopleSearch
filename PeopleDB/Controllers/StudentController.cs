        [HttpGet]
        public string GetPeopleList()
        {
            string result = string.Empty;
            List<PeopleNames> lstStates = new List<PeopleNames>();
            PeopleNames objStates = null;

            var students = from s in db.Students
                           select s;
            foreach (var s in students)
            {
                objStates = new PeopleNames();
                objStates.Names = s.FirstMidName;
                lstStates.Add(objStates);
            }

            
            //json stringfy the result
            result = new System.Web.Script.Serialization.JavaScriptSerializer().Serialize(lstStates);
            return result;
        }

       
       
